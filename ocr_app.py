def smart_read(image_pil):
    # à¹à¸›à¸¥à¸‡ PIL à¹€à¸›à¹‡à¸™ OpenCV format (numpy)
    img_np = np.array(image_pil)
    
    # à¸–à¹‰à¸²à¸ à¸²à¸žà¹€à¸›à¹‡à¸™à¸ªà¸µ à¹ƒà¸«à¹‰à¹à¸›à¸¥à¸‡à¹€à¸›à¹‡à¸™ Grayscale à¹€à¸žà¸·à¹ˆà¸­à¸¥à¸” Noise à¸ªà¸µà¸žà¸·à¹‰à¸™à¸«à¸¥à¸±à¸‡ (à¸Šà¹ˆà¸§à¸¢à¹ƒà¸«à¹‰à¸­à¹ˆà¸²à¸™à¸ªà¸µà¸”à¸³à¸Šà¸±à¸”à¸‚à¸¶à¹‰à¸™)
    if len(img_np.shape) == 3:
        img_gray = cv2.cvtColor(img_np, cv2.COLOR_RGB2GRAY)
    else:
        img_gray = img_np

    # à¹€à¸žà¸´à¹ˆà¸¡ Contrast (Adaptive Threshold) à¹€à¸žà¸·à¹ˆà¸­à¹ƒà¸«à¹‰à¸•à¸±à¸§à¸«à¸™à¸±à¸‡à¸ªà¸·à¸­à¸”à¸³à¸¥à¸­à¸¢à¹€à¸”à¹ˆà¸™à¸‚à¸¶à¹‰à¸™à¸¡à¸²à¸ˆà¸²à¸à¸žà¸·à¹‰à¸™à¸«à¸¥à¸±à¸‡
    # à¹€à¸—à¸„à¸™à¸´à¸„à¸™à¸µà¹‰à¸Šà¹ˆà¸§à¸¢à¹à¸à¹‰à¸›à¸±à¸à¸«à¸²à¹à¸ªà¸‡à¹€à¸‡à¸²à¸šà¸™à¸à¸²à¸‚à¸§à¸”
    img_processed = cv2.adaptiveThreshold(
        img_gray, 255, cv2.ADAPTIVE_THRESH_GAUSSIAN_C, cv2.THRESH_BINARY, 11, 2
    )

    candidates = []

    # à¸§à¸™à¸¥à¸¹à¸›à¸«à¸¡à¸¸à¸™à¸ à¸²à¸ž 4 à¸—à¸´à¸¨ (0, 90, 180, 270 à¸­à¸‡à¸¨à¸²)
    # à¹€à¸žà¸£à¸²à¸°à¹€à¸£à¸²à¹„à¸¡à¹ˆà¸£à¸¹à¹‰à¸§à¹ˆà¸² user à¸–à¹ˆà¸²à¸¢à¸¡à¸²à¸¡à¸¸à¸¡à¹„à¸«à¸™
    for angle in [0, 90, 180, 270]:
        rotated_img = rotate_image(img_processed, angle) # à¸ªà¹ˆà¸‡à¸ à¸²à¸žà¸‚à¸²à¸§à¸”à¸³à¸—à¸µà¹ˆ process à¹à¸¥à¹‰à¸§à¹„à¸›à¸­à¹ˆà¸²à¸™
        
        # à¸­à¹ˆà¸²à¸™à¸„à¹ˆà¸² (detail=0 à¹€à¸­à¸²à¹à¸„à¹ˆ text)
        results = reader.readtext(rotated_img, detail=0)
        
        # à¸£à¸§à¸¡à¸œà¸¥à¸¥à¸±à¸žà¸˜à¹Œà¸—à¸¸à¸à¸šà¸£à¸£à¸—à¸±à¸”à¹ƒà¸™à¸ à¸²à¸žà¸¡à¸¸à¸¡à¸™à¸±à¹‰à¸™à¹€à¸‚à¹‰à¸²à¸”à¹‰à¸§à¸¢à¸à¸±à¸™
        full_text = "".join(results)
        
        # --- à¸à¸£à¸­à¸‡à¹€à¸‚à¹‰à¸¡à¸‡à¸§à¸” (Regex) ---
        # 1. à¹à¸›à¸¥à¸‡à¹€à¸›à¹‡à¸™à¸žà¸´à¸¡à¸žà¹Œà¹ƒà¸«à¸à¹ˆà¸—à¸±à¹‰à¸‡à¸«à¸¡à¸”
        full_text = full_text.upper()
        # 2. à¹€à¸à¹‡à¸šà¹€à¸‰à¸žà¸²à¸° A-Z à¹à¸¥à¸° 0-9 à¹€à¸—à¹ˆà¸²à¸™à¸±à¹‰à¸™ (à¸•à¸±à¸”à¸Šà¹ˆà¸­à¸‡à¸§à¹ˆà¸²à¸‡à¹à¸¥à¸°à¸ªà¸±à¸à¸¥à¸±à¸à¸©à¸“à¹Œà¸—à¸´à¹‰à¸‡)
        clean_text = re.sub(r'[^A-Z0-9]', '', full_text)
        
        # 3. à¹€à¸Šà¹‡à¸„à¸„à¸§à¸²à¸¡à¸¢à¸²à¸§
        # à¹€à¸£à¸²à¸•à¹‰à¸­à¸‡à¸à¸²à¸£ 9 à¸•à¸±à¸§à¸­à¸±à¸à¸©à¸£à¹€à¸›à¹Šà¸°à¹† 
        # à¸šà¸²à¸‡à¸—à¸µà¸¡à¸±à¸™à¸­à¸²à¸ˆà¸ˆà¸°à¸­à¹ˆà¸²à¸™à¹€à¸à¸´à¸™à¸¡à¸² à¹€à¸£à¸²à¸ˆà¸°à¸¥à¸­à¸‡à¸«à¸² Pattern 9 à¸•à¸±à¸§
        matches = re.findall(r'[A-Z0-9]{9}', clean_text)
        
        for match in matches:
            return match # à¹€à¸ˆà¸­ 9 à¸•à¸±à¸§à¹€à¸›à¹Šà¸°à¹† à¸ªà¹ˆà¸‡à¸à¸¥à¸±à¸šà¸—à¸±à¸™à¸—à¸µ (à¸ˆà¸šà¸‡à¸²à¸™à¹€à¸£à¹‡à¸§)

        # à¸–à¹‰à¸²à¹„à¸¡à¹ˆà¹€à¸ˆà¸­ 9 à¸•à¸±à¸§à¹€à¸›à¹Šà¸° à¹ƒà¸«à¹‰à¹€à¸à¹‡à¸šà¸„à¹ˆà¸²à¸—à¸µà¹ˆà¹ƒà¸à¸¥à¹‰à¹€à¸„à¸µà¸¢à¸‡à¹„à¸§à¹‰à¸à¹ˆà¸­à¸™
        if len(clean_text) >= 7 and len(clean_text) <= 12:
             candidates.append(clean_text)

    # à¸–à¹‰à¸²à¸§à¸™à¸„à¸£à¸š 4 à¸—à¸´à¸¨à¹à¸¥à¹‰à¸§à¸¢à¸±à¸‡à¹„à¸¡à¹ˆà¹€à¸ˆà¸­ 9 à¸•à¸±à¸§à¹€à¸›à¹Šà¸°à¹† à¹ƒà¸«à¹‰à¹€à¸­à¸²à¸„à¹ˆà¸²à¸—à¸µà¹ˆà¸¢à¸²à¸§à¸—à¸µà¹ˆà¸ªà¸¸à¸”à¸—à¸µà¹ˆà¸«à¸²à¹„à¸”à¹‰ (Best Guess)
    if candidates:
        # à¹€à¸¥à¸·à¸­à¸à¸•à¸±à¸§à¸—à¸µà¹ˆà¸„à¸§à¸²à¸¡à¸¢à¸²à¸§à¹ƒà¸à¸¥à¹‰à¹€à¸„à¸µà¸¢à¸‡ 9 à¸—à¸µà¹ˆà¸ªà¸¸à¸”
        return max(candidates, key=len)
    
    return None

